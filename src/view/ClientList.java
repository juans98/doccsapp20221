    private void clienteDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clienteDataBtnActionPerformed
        ClientDAOImp clientDAOImp = new ClientDAOImp();
        //Se traen los datos de la BD
        try {
            listClient = (ArrayList<Client>) clientDAOImp.listClients();
            Object[] data = new Object[dtmClients.getColumnCount()];
            int i = 1;
            dtmClients.setRowCount(0);
            for (Client client : listClient) {
                data[0] = client.getId();
                data[1] = client.getFullName();
                data[2] = client.getAddress();
                data[3] = client.getEmail();
                data[4] = client.getPhone();
                data[5] = client.isState();
                data[6] = client.getType();
                data[7] = client.getNotes();
                i++;
                dtmClients.addRow(data);
            }
            clientTbl.setModel(dtmClients);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_clienteDataBtnActionPerformed

