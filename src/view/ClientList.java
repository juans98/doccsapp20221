    private void clientDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clientDeleteBtnActionPerformed
        int index = clientTbl.getSelectedRow();
        Client client = listClient.get(index);
        JOptionPane.showMessageDialog(null, client.getId());
        ClientDAOImp clientDAOImp = new ClientDAOImp();
        int rta = JOptionPane.showConfirmDialog(null, "Do you want to delete the record?",
                "Remove Client Registry", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
        if (rta == 0) {
            try {
                clientDAOImp.deleteClient(client);
                JOptionPane.showMessageDialog(null, "Deleted Client Record");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Operation Not Performed");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operation Not Performed");
        }
        if (dtmClients.getRowCount() > 0) {
            for (int i = dtmClients.getRowCount() - 1; i > -1; i--) {
                dtmClients.removeRow(i);
            }
            try {
                var clients = clientDAOImp.listClients();
                Object[] data = new Object[dtmClients.getColumnCount()];
                int i = 1;
                dtmClients.setRowCount(0);
                for (Client c : clients) {
                    data[0] = c.getId();
                    data[1] = c.getFullName();
                    data[2] = c.getAddress();
                    data[3] = c.getEmail();
                    data[4] = c.getPhone();
                    data[5] = c.isState();
                    data[6] = c.getType();
                    data[7] = c.getNotes();
                    i++;
                    dtmClients.addRow(data);
                }
                clientTbl.setModel(dtmClients);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_clientDeleteBtnActionPerformed

    
